MOV AH,0AH           ;
LEA DX, ENTRADA3     ;
INT 21H              ;
RET                  ;
TECLA3 ENDP          ;
;_____________________________________

CAMPANA3 PROC NEAR   ;
MOV BX,00H           ;
MOV BL, LONREAL3     ;
MOV INTRO3[BX],07H   ;
MOV INTRO3[BX+1],'$' ;
RET                  ;
CAMPANA3 ENDP        ;
;_____________________________________

